    public void startScanning() {
        System.out.println("start scanning");
        btScanning = true;
        deviceIndex = 0;
        devicesDiscovered.clear();
        peripheralTextView.setText("");
        peripheralTextView.append("Started Scanning\n");
        startScanningButton.setVisibility(View.INVISIBLE);
        stopScanningButton.setVisibility(View.VISIBLE);
        AsyncTask.execute(new Runnable() {
            @Override
            public void run() {
                btScanner.startScan(leScanCallback);
            }
        });

        mHandler.postDelayed(new Runnable() {
            @Override
            public void run() {
                stopScanning();
            }
        }, SCAN_PERIOD);
    }

    public void stopScanning() {
        System.out.println("stopping scanning");
        peripheralTextView.append("Stopped Scanning\n");
        btScanning = false;
        startScanningButton.setVisibility(View.VISIBLE);
        stopScanningButton.setVisibility(View.INVISIBLE);
        AsyncTask.execute(new Runnable() {
            @Override
            public void run() {
                btScanner.stopScan(leScanCallback);
            }
        });
    }

    public void connectToDeviceSelected() {
        peripheralTextView.append("Trying to connect to device at index: " + deviceIndexInput.getText() + "\n");
        int deviceSelected = Integer.parseInt(deviceIndexInput.getText().toString());
        bleDevice = devicesDiscovered.get(deviceSelected);

        if (bleDevice != null) {
            long delayMillis = 0L;
            if (bleDevice.getBondState() == BluetoothDevice.BOND_NONE) {
                delayMillis = 5000L;
                boolean bondResult = bleDevice.createBond();
                peripheralTextView.append("Bonding with device at index: " + deviceIndexInput.getText() + " bond start result: " + bondResult+ "\n");
            }
            mHandler.postDelayed(new Runnable() {
                @Override
                public void run() {
                    final int bondState = bleDevice.getBondState();
                    peripheralTextView.append("1. FIRST TIME Check state: "+bondState+"\n");
                    if (bondState == BluetoothDevice.BOND_BONDED) {
                        peripheralTextView.append("Inside If\n");
                        bluetoothGatt = bleDevice.connectGatt(getActivity(), true, btleGattCallback);
                    }else if(bondState == BluetoothDevice.BOND_BONDING){
                        long newDelayMills = 5000L;
                        mHandler.postDelayed(new Runnable() {
                            @Override
                            public void run() {
                                int newBondState = bleDevice.getBondState();
                                peripheralTextView.append("2. SECOND TIME Check state: "+newBondState+"\n");
                                if (newBondState == BluetoothDevice.BOND_BONDED) {
                                    bluetoothGatt = bleDevice.connectGatt(getActivity(), true, btleGattCallback);
                                }
                            }
                        }, newDelayMills);
                    }
                }
            }, delayMillis);
        }
    }

    public void disconnectDeviceSelected() {
        peripheralTextView.append("Disconnecting from device\n");
        bluetoothGatt.disconnect();
    }

    private void displayGattServices(List<BluetoothGattService> gattServices) {
        if (gattServices == null) return;

        // Loops through available GATT Services.
        for (BluetoothGattService gattService : gattServices) {

            final String uuid = gattService.getUuid().toString();
            System.out.println("Service discovered: " + uuid);
            getActivity().runOnUiThread(new Runnable() {
                public void run() {
                    peripheralTextView.append("Service discovered: "+uuid+"\n");
                }
            });
            new ArrayList<HashMap<String, String>>();
            List<BluetoothGattCharacteristic> gattCharacteristics =
                    gattService.getCharacteristics();

            // Loops through available Characteristics.
            for (BluetoothGattCharacteristic gattCharacteristic :
                    gattCharacteristics) {

                final String charUuid = gattCharacteristic.getUuid().toString();
                System.out.println("Characteristic discovered for service: " + charUuid);
                getActivity().runOnUiThread(new Runnable() {
                    public void run() {
                        peripheralTextView.append("Characteristic discovered for service: "+charUuid+"\n");
                    }
                });

            }
        }
    }

